				line.data("tooltip", tooltip);
				//line.data("don", connects[i][0]);
				line.data("sMP",connects[i][startIndex]);
				line.data("eMP",connects[i][count]);
				
				connLines.push(line);
				paraG.seats[connects[i][startIndex]][0].data("cons").push(line);
				paraG.seats[connects[i][count]][0].data("cons").push(line);
				
