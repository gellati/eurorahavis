function row(cent, side, dist){
	var ang;
	if(side==0){
		ang = 20/cent;
	}else{
		ang = 22.0/cent;
	}
		var angSum;
		var ob;
		
		angSum = ang/2+2 + cent*ang + (ang/3+3);
		for(var i = side-1;i>=0;i--){
			var ob = {};
			ob.x = Math.sin(Raphael.rad(angSum+i*ang))*dist;
			ob.y = -Math.cos(Raphael.rad(angSum+i*ang))*dist;
			ob.rot = angSum+i*ang-180;
		 	paraG.MPs.push(ob);
			}
			angSum = ang/2+2;
		for(var i = cent-1;i>=0;i--){
			var ob = {};
			ob.x = Math.sin(Raphael.rad(angSum+i*ang))*dist;
			ob.y = -Math.cos(Raphael.rad(angSum+i*ang))*dist;
			ob.rot = angSum+i*ang-180;
		 	paraG.MPs.push(ob);
			}
			angSum = -(ang/2+2);
		for(var i = 0;i<cent;i++){
			var ob = {};
			ob.x = Math.sin(Raphael.rad(angSum-i*ang))*dist;
			ob.y = -Math.cos(Raphael.rad(angSum-i*ang))*dist;
			ob.rot = angSum-i*ang-180;
		 	paraG.MPs.push(ob);
			}
			angSum = angSum - cent*ang - (ang/3+3);
		var modSide;	
		if(cent==8 && side==6){modSide = side+1;}else modSide=side;
		for(var i = 0;i<modSide;i++){
			var ob = {};
			ob.x = Math.sin(Raphael.rad(angSum-i*ang))*dist;
			ob.y = -Math.cos(Raphael.rad(angSum-i*ang))*dist;
			ob.rot = angSum-i*ang-180;
		 	paraG.MPs.push(ob);
			}
			
		
}

